#include <stdio.h>
int main()
{
   //integer value for short int in a
   short int a=324;
   //integer value with unsigned int in b
   int b=20U;
   //integer value with positive data
   int c=25;
   //integer with negitive value
   int d=-58;
   //given float value ine
   float e=2.7f;
   printf("integer value for short int:%hd\n",a);
   printf("integer value with unsigned int:%u\n",b);
   printf("integer value with positive data: %d\n",c);
   printf("integer value with negative value: %d\n",d);
   printf("integer value with float value:%f\n",e);
   return 0;
}


